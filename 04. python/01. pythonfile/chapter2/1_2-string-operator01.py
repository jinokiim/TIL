# "  "와 '  '를 동시에 이용하여 표현
print("my name is 'Jinho'")
print() 

# '  ' 사용하여 표현 ("  " 만 일때도 동일)
print('my name is \'Jinho\'')
print()

# \n -> 줄바꿈
# \t -> 탭
print("name\tage\tcountry")
print("Jinho\t27\tKorea")
print("Sivila\t23\tItaly")
print("Mike\t24\tUSA")
print("Rodrigo\t29\tSpain")
print()

